public class SellingInfo {

    private String sellerName;
    private String noOfAvailableQuantity;
    private String productCondition;
    private String deliveryDate;
    private String productColor;
    private String productSize;

    public String getSellerName() {
        return sellerName;
    }

    public void setSellerName(String sellerName) {
        this.sellerName = sellerName;
    }

    public String getNoOfAvailableQuantity() {
        return noOfAvailableQuantity;
    }

    public void setNoOfAvailableQuantity(String noOfAvailableQuantity) {
        this.noOfAvailableQuantity = noOfAvailableQuantity;
    }

    public String getProductCondition() {
        return productCondition;
    }

    public void setProductCondition(String productCondition) {
        this.productCondition = productCondition;
    }

    public String getDeliveryDate() {
        return deliveryDate;
    }

    public void setDeliveryDate(String deliveryDate) {
        this.deliveryDate = deliveryDate;
    }

    public String getProductColor() {
        return productColor;
    }

    public void setProductColor(String productColor) {
        this.productColor = productColor;
    }

    public String getProductSize() {
        return productSize;
    }

    public void setProductSize(String productSize) {
        this.productSize = productSize;
    }
}
